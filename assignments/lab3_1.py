# Complete this program to classify people by age
age = int(input("Enter age: "))
if age<=12 and age>0 :
    print("You age is ",age,"you child.")
elif age>=13 :
    print("You age is ",age,"you teenager.")
elif age>=20 :
    print("You age is ",age,"you adult.")
elif age>=60 :
    print("You age is ",age,"you senior.")
else :
    print("This not age.")
    



